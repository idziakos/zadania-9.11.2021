"Typ 'Number' - typ liczbowy"
let x = 15
let y = 2
let z = 9.5
"Zmienna typu Number może przechowywać liczbę. W odróżnieniu od innych języków programowania nie rozróżniamy tutaj typów liczb całkowitych lub rzeczywistych."

"Typ 'String' - typ tekstowy"
let a = "Bogdan"
let b = "Adrian"
let c = 'Witam serdecznie'
"Jest to typ zmiennej, która zawiera tekst - ciąg znaków. Wartość ciągu może być łańcuchem zero lub więcej znaków Unicode (liter, cyfr i znaków interpunkcyjnych)."

"Typ 'Boolean' - typ logiczny"
"Jest to typ zmiennej, która może przyjmować tylko dwie wartości: True - prawda i False - fałsz."

"Typ 'null'"
"Jest to typ danych, który ma tylko jedną wartość - null, czyli nic, pusty."
"Przypisanie wartości null zmiennej powoduje wymazanie jej wartości."